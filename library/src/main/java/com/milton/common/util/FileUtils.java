    /**
     * 字节数转以unit为单位的size
     *
     * @param byteNum 大小
     * @param unit    <ul>
     *                <li>{@link Constants.MemoryUnit#BYTE}: 字节</li>
     *                <li>{@link Constants.MemoryUnit#KB}  : 千字节</li>
     *                <li>{@link Constants.MemoryUnit#MB}  : 兆</li>
     *                <li>{@link Constants.MemoryUnit#GB}  : GB</li>
     *                </ul>
     * @return 以unit为单位的size
     */
    public static double byte2Size(long byteNum, Constants.MemoryUnit unit) {
        switch (unit) {
            default:
            case BYTE:
                return (double) byteNum / BYTE;
            case KB:
                return (double) byteNum / KB;
            case MB:
                return (double) byteNum / MB;
            case GB:
                return (double) byteNum / GB;
        }
    }

